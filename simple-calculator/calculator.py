a = int(input("Enter First Number "))
choice = input("Enter Operation to be performed ")
b = int(input("Enter Second Number "))
if(choice=="+"):
    print("The addition of given numbers is ",a+b)
elif(choice=="*"):
    print("The Multiplication of given numbers is ",a*b)
elif(choice=="-"):
    print("The subtraction of given numbers is ",a-b)
elif(choice=="/"):
    print("The division of two numbers is ",a/b)
